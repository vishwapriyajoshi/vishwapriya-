PlasticSales <- read_csv("C:/Users/Admin/Downloads/PlasticSales.csv")
View(PlasticSales)
X <- data.frame(outer(rep(month.abb,length= 60),month.abb,"==")+0)
View(X)
colnames(X)<- month.abb
View(X)
cons<- cbind(PlasticSales,X)
View(cons)
cons["t"]<- 1:60
View(cons)
cons["log_Sales"]<-log(cons["Sales"])
cons["t_square"]<-cons["t"]*cons["t"]
attach(cons)
train<- cons[1:48,]
test<- cons[49:60,]

##########LINIER model ##############
lm_model <- lm(Sales~t, data = train)
summary(lm_model)
linear_pred<-data.frame(predict(lm_model,interval='predict',newdata =test))
View(linear_pred)
rmse_linear<-sqrt(mean((test$Sales-linear_pred$fit)^2,na.rm = T))
rmse_linear




